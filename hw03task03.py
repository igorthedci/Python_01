# Попробуйте вывести числа в одной строчке, разделённые пробелом

sss = "0"
num = 1
while num < 11 :
    sss += " " + str(num)
    num += 1
print(sss)

sss = "20"
num = 19
while num > 0 :
    sss += " " + str(num)
    num -= 1
print(sss)

